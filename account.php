            <p>
              The Ingredients :<br>
              4 Servings<br>
              500 gm pasta penne<br>
              1 capsicum (green pepper)<br>
              2 tablespoon virgin olive oil<br>
              1 teaspoon ginger paste<br>
              Water as required<br>
              2 tomato<br>
              7 pieces green onion<br>
              1 carrot<br>
              1 teaspoon garlic paste<br></p>
              <p> The Instructions:<br></p>
 <p>Step 1 :<br>
Take a deep pan, add pasta and enough water in it. Sprinkle salt and heat it over medium flame. Boil it till the pasta turns tender and is slightly cooked. Once cooked, drain the water and transfer pasta in a bowl. Keep it aside. Meanwhile, take a chopping mat and chop all the vegetables, one by one.<br></p>

<p>Step 2 :<br>
In a pressure cooker, add chopped tomatoes, carrots and capsicums. Pour little water, close the lid and boil vegetables on a medium flame. When vegetables are slightly cooked, transfer them in a blender jar and blend them.<br></p>

<p>Step 3 :<br>
Now, take a non-stick pan, add oil in it and heat it over medium flame. When oil bubbles rise, add ginger and garlic paste along with chopped green onions. Sprinkle oregano and saute for a minute or two.<br></p>

<p>Step 4 :<br>
Pour the blended vegetables in saucepan, add chilli flakes and salt to it. Stir-fry this mixture for some time and then add boiled pasta to it. Sprinkle some oregano over this and mix the ingredients well. Garnish with shredded paneer and serve it hot.<br>
              </p>
